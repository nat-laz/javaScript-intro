// Initial value assigned
let result = 10;

// Arithmetic + Assignment: Adding 5 to the result
result += 5; // Same as: result = result + 5
console.log(result); // Output: 15

// Arithmetic + Assignment: Subtracting 3 from the result
result -= 3; // Same as: result = result - 3
console.log(result); // Output: 12

// Arithmetic + Assignment: Multiplying the result by 2
result *= 2; // Same as: result = result * 2
console.log(result); // Output: 24

// Arithmetic + Assignment: Dividing the result by 4
result /= 4; // Same as: result = result / 4
console.log(result); // Output: 6

// Arithmetic + Assignment: Modulus operation (remainder of result / 5)
result %= 5; // Same as: result = result % 5
console.log(result); // Output: 1
