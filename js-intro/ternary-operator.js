// Simple if-else
let age = 16;

if (age >= 18) {
  console.log("You are an adult.");
} else {
  console.log("You are a minor.");
}

// Ternary - operator
// Syntax:     condition ?     expression1    :   expression2
let message = age >= 18 ? "You are an adult." : "You are a minor.";
console.log(message);
